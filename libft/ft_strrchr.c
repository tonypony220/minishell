#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *p;

	p = 0;
	while (*s || *s == (char)c)
	{
		if (*s == (char)c)
			p = (char*)s;
		if (!*s)
			return (p);
		s++;
	}
	return (p);
}
